    private void connect(String ip, int port) {
        
        // connect to server
        new Thread(() -> {
            // call controller
            boolean hasConnected = AutoBattler.socketHandler.connect(ip, port);            
            System.out.println("Connection Thread Initialization Result: " + hasConnected);
           
        }).start();
    }
    
    

